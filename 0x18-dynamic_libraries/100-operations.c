int add(int i, int k);
int sub(int i, int k);
int mul(int i, int k);
int div(int i, int k);
int mod(int i, int k);

int add(int a,int b)
{
	return a + b;
}

int sub(int a,int b)
{
	return a - b;
}

int mul(int a,int b)
{
	return a * b;
}


int div(int a,int b)
{
	return a / b;
}

int mod(int a,int b)
{
	return a % b;
}
